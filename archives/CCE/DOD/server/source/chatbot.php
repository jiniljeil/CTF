<script>

function createSendDiv(chat) { 
  let outgoing_msg =  document.createElement("div");
  outgoing_msg.setAttribute("class", "outgoing_msg");
  
  let sent_msg =  document.createElement("div");
  sent_msg.setAttribute("class", "sent_msg");
  outgoing_msg.appendChild(sent_msg);

  let contentp =  document.createElement("p");
  contentp.innerHTML = chat;

  let date = document.createElement("span");
  date.setAttribute("class", "time_date");
  date.innerHTML = new Date().toLocaleTimeString('en');
  
  sent_msg.appendChild(contentp);
  sent_msg.appendChild(date);
  $(".msg_history")[0].appendChild(outgoing_msg);
}

function createRecvDiv(chat) { 
  let incoming_msg = document.createElement("div");
  incoming_msg.setAttribute("class", "incoming_msg");

  let incoming_msg_img =  document.createElement("div");
  incoming_msg_img.setAttribute("class", "incoming_msg_img");
  let img = document.createElement("img");
  img.src = "/static/img/chatbot_icon.png";

  incoming_msg_img.appendChild(img);
  incoming_msg.appendChild(incoming_msg_img);

  let received_msg =  document.createElement("div");
  received_msg.setAttribute("class", "received_msg");
  incoming_msg.appendChild(received_msg);

  let received_withd_msg =  document.createElement("div");
  received_withd_msg.setAttribute("class", "received_withd_msg");
  received_msg.appendChild(received_withd_msg);

  let contentp =  document.createElement("p");
  contentp.innerHTML = chat;

  let date = document.createElement("span");
  date.setAttribute("class", "time_date");
  date.innerHTML = new Date().toLocaleTimeString('en');
  
  received_withd_msg.appendChild(contentp);
  received_withd_msg.appendChild(date);
  $(".msg_history")[0].appendChild(incoming_msg);
}

function sendChat(chat) {
  if(chat != undefined) {
    var send_content = chat;
  } else {
    var send_content = $(".write_msg")[0].value;
  }
  createSendDiv(send_content);
  let body = $(".msg_history")[0];
  body.scrollTo(0, body.scrollHeight);

  $.post("/chatbot_proc.php", {"cmd": send_content}, function(result) {
    createRecvDiv(result);
    body.scrollTo(0, body.scrollHeight);
  });
  //update scroll position

  body.scrollTo(0, body.scrollHeight);
}
